#include<stdio.h>
int main()
{
char a[100],b[100],d[100];
int i,j,l=0,c=0,k,p;
scanf("%s %s",&a,&b);
for(i=0;a[i]!='\0';i++)
{
l++;
}
//printf("%d\n",l);
for(j=0;b[j]!='\0';j++)
{
c++;
}
//printf("%d\n",c);
if(l==c)
printf("%s%s",a,b);
else
{
k=(l>c)?l:c;
//printf("%d\n",k);
if(k==l)
{
p=0;
for(i=0;i<c;i++)
{
d[p]=a[i];
p++;
}
for(j=0;j<c;j++)
{
d[p]=b[j];
p++;
}
for(p=0;p<c+c;p++)
{
printf("%c",d[p]);
}
}
if(k==c)
{
p=0;
for(i=0;i<l;i++)
{
d[p]=a[i];
p++;
}
for(j=0;j<l;j++)
{
d[p]=b[j];
p++;
}
for(p=0;p<l+l;p++)
{
printf("%c",d[p]);
}
}
}
}
